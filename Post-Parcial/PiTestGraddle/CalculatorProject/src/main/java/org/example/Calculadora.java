package org.example;

public class Calculadora {
    public int add(int a,int b){
        return a+b;
    }

    public int substract(int a,int b){
        return a-b;
    }

    public int multiplication(int a,int b){
        return a*b;
    }

    public int division(int a,int b){
        return a/b;
    }

}
